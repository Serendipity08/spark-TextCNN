const blessingHints = {
  BIRTHDAY: ["这一年TA最不容易的地方", "你希望TA拥有的一个小愿望"],
  FESTIVAL: ["节日里的小期待", "你们共同的节日记忆"],
  THANKS: ["最想感谢的一件事", "你想回应的心意"],
  CONGRATS: ["对方值得被肯定的地方", "你想鼓励对方继续的方向"]
};

export function buildHints(recipientType, blessingPreset) {
  const hints = [];
  if (blessingPreset && blessingPreset !== "NONE" && blessingHints[blessingPreset]) {
    hints.push(...blessingHints[blessingPreset]);
  }
  if (recipientType && recipientType !== "NONE" && recipientHints[recipientType]) {
    hints.push(...recipientHints[recipientType]);
  }
  if (!hints.length) {
    hints.push(...baseHints);
  }
  return hints.slice(0, 2);
}
